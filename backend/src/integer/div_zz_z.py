from typing import Self

class Div_zz_z:
    def div_zz_z(self: Self, other: Self) -> Self:
        """
        Нахождение частного от деления целого числа на целое
        Возвращает целое число со знаком
        Автор: Рубан Егор гр. 4383
        """

        # Проверяем деление на ноль
        # poz_z_d == 0 означает, что другое число равно 0
        if other.poz_z_d == 0:
            raise ZeroDivisionError("Деление на ноль невозможно")

        # Получаем модули делимого и делителя
        # abs_z_n возвращает натуральное число
        module1 = self.abs_z_n()
        module2 = other.abs_z_n()

        # Выполняем деление модулей
        # DIV_NN_N возвращает натуральное число — модуль частного
        q = module1.DIV_NN_N(module2)

        # Определяем знак результата
        # sign = 0 — положительное число
        # sign = 1 — отрицательное число
        # Если оба числа имеют одинаковый знак — результат положительный
        if (self.sign == other.sign):
            sign = 0
        else:
            # Если числа имеют разные знаки, результат отрицательный
            # Исключение — когда делимое равно нулю: 0 / a = 0, знак должен быть 0
            if not self.is_zero():
                sign = 1
            else: 
                sign = 0

        # Возвращаем новое целое число: знак + модуль
        return self.__class__(sign, q)
