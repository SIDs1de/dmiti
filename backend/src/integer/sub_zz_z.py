from typing import Self

class Sub_zz_z:
    def sub_zz_z(self: Self, second: Self) -> Self:
        """
        Приготовил: Viktor Permitin
        Вычитание целых чисел
        
        Параметры:
            self: Self - уменьшаемое целое число
            second: Self - вычитаемое целое число
        
        Возвращает:
            Self - разность двух целых чисел (self - second)
        
        Алгоритм:
            Вычитание реализуется через сложение с противоположным числом:
            a - b = a + (-b)
            
            1. Умножаем вычитаемое на -1 с помощью mul_zm_z()
               (меняем знак вычитаемого)
            2. Складываем уменьшаемое с противоположным вычитаемым
               с помощью add_zz_z()
        
        Примеры:
            sub_zz_z(5, 3) = 2
            sub_zz_z(5, -3) = 8
            sub_zz_z(-5, 3) = -8
            sub_zz_z(-5, -3) = -2
            sub_zz_z(5, 0) = 5
            sub_zz_z(0, 3) = -3
        
        Используется в:
            - SUB_QQ_Q (вычитание рациональных чисел)
            - MOD_ZZ_Z (остаток от деления целых чисел)
            - SUB_PP_P (вычитание многочленов)
        
        Примечание:
            Этот метод является оберткой над add_zz_z, что упрощает реализацию
            и обеспечивает согласованность операций. Все сложные случаи
            (разные знаки, нули) обрабатываются в методе add_zz_z.
        """
        # Вычитание реализуется как сложение с противоположным числом
        # self - second = self + (-second)
        # mul_zm_z() умножает число на -1 (меняет знак)
        return self.add_zz_z(second.mul_zm_z())
