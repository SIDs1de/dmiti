from typing import Self

class LCM_NN_N:
    def LCM_NN_N(self: Self, other: Self) -> Self:
        """
        Метод нахождения НОК двух натуральных чисел.
        Результат - натуральное число
        Автор: Рубан Егор гр. 4383
        """

        # Находим НОД двух чисел
        # GCF_NN_N возвращает наибольший общий делитель
        G = self.GCF_NN_N(other)

        # Вычисляем произведение двух натуральных чисел
        # MUL_NN_N возвращает натуральное число — результат умножения self * other
        P = self.MUL_NN_N(other)

        # Делим произведение P на НОД G
        # Это стандартная формула НОК: LCM(a, b) = a * b / GCD(a, b)
        L = P.DIV_NN_N(G)

        # Возвращаем найденный НОК
        return L

